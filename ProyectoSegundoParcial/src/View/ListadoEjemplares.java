    private void cargarDatos(){
        modeloTabla.setRowCount(0);
        List<Object>Ejemplares = ejemplaresDAO.getAll();
        for(Object Ejemplares_uncast : Ejemplares){
            Ejemplares ejemplares = (Ejemplares) Ejemplares_uncast;
            modeloTabla.addRow(new Object[]{ejemplares.getEjemplarID(), ejemplares.getLibroID(), ejemplares.getFechaIngreso(), ejemplares.getEstado()});
        }
    }

    
    public void refrescarInfo(){
        cargarDatos();
        
    }
    
