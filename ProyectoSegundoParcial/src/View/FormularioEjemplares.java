    private void btn_AccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AccionActionPerformed
        // TODO add your handling code here:
        if(mode.equals("INS")){
            Ejemplares ejemplares = new Ejemplares();
            try {
            ejemplares.setLibroID(Integer.parseInt(txt_LibroID.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID de Libros debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
            
            try {
            java.sql.Date fecha = java.sql.Date.valueOf(txt_FechaIngreso.getText()); // debe estar en formato yyyy-MM-dd
            ejemplares.setFechaIngreso(fecha);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "La fecha debe tener el formato yyyy-MM-dd", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

            
        try {
            ejemplares.setEstado(Integer.parseInt(txt_Estado.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID de Estado debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
            dao.insert(ejemplares);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
        
        if(mode.equals("UPD")){
             Ejemplares ejemplares = new Ejemplares();
            try {
            ejemplares.setLibroID(Integer.parseInt(txt_LibroID.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID de Libros debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
            
            try {
            java.sql.Date fecha = java.sql.Date.valueOf(txt_FechaIngreso.getText()); // debe estar en formato yyyy-MM-dd
            ejemplares.setFechaIngreso(fecha);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "La fecha debe tener el formato yyyy-MM-dd", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

            
        try {
            ejemplares.setEstado(Integer.parseInt(txt_Estado.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID de Estado debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        
        }
            dao.update(ejemplares);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
        
        if(mode.equals("DLT")){
            dao.delete(EjemplarID);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
    }//GEN-LAST:event_btn_AccionActionPerformed

