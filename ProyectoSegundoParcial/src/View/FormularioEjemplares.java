    public FormularioEjemplares(ListadoEjemplares pParent, boolean modal, String pMode, int pEjemplarID) {
        super(pParent, modal);
        this.parent = pParent;
        initComponents();
        
        setLocationRelativeTo(null);
                
        dao = new EjemplaresDAO();
        this.mode = pMode;
        this.EjemplarID = pEjemplarID;
        txt_EjemplarID.setText(String.valueOf(EjemplarID));
        if(mode.equals("INS")){
            btn_Accion.setText("Insertar");
        }else if(mode.equals("UPD")){
            Ejemplares ejemplares = (Ejemplares) dao.getByEjemplarID(EjemplarID);
            txt_LibroID.setText(String.valueOf(ejemplares.getLibroID()));
            txt_FechaIngreso.setText(String.valueOf(ejemplares.getFechaIngreso()));
            txt_Estado.setText(String.valueOf(ejemplares.getEstado()));
            
            
            btn_Accion.setText("Actualizar");
            
        }else if(mode.equals("DLT")){
            Ejemplares ejemplares = (Ejemplares) dao.getByEjemplarID(EjemplarID);
            txt_LibroID.setText(String.valueOf(ejemplares.getLibroID()));
            txt_LibroID.setEnabled(false);
            txt_FechaIngreso.setText(String.valueOf(ejemplares.getFechaIngreso()));
            txt_FechaIngreso.setEnabled(false);
            txt_Estado.setText(String.valueOf(ejemplares.getEstado()));
            txt_Estado.setEnabled(false);
            btn_Accion.setText("Eliminar");
        }
    }

