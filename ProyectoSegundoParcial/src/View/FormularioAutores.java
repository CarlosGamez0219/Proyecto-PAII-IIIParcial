    public FormularioAutores(ListadoAutores pParent, boolean modal, String pMode, int pAutorID) {
        super(pParent, modal);
        this.parent = pParent;
        initComponents();
        setLocationRelativeTo(null);
        
         dao = new AutoresDAO();
        this.mode = pMode;
        this.AutorID = pAutorID;
        txt_AutorID.setText(String.valueOf(pAutorID));
        if(mode.equals("INS")){
            btn_Accion.setText("Insertar");
        }else if(mode.equals("UPD")){
            Autores autores = (Autores) dao.getByAutorID(AutorID);
            txt_NombreAutor.setText(autores.getNombreAutor());
            txt_ApellidoAutor.setText(autores.getApellidoAutor());
            
            btn_Accion.setText("Actualizar");
            
        }else if(mode.equals("DLT")){
            Autores autores = (Autores) dao.getByAutorID(AutorID);
            txt_NombreAutor.setText(autores.getNombreAutor());
            txt_NombreAutor.setEnabled(false);
            txt_ApellidoAutor.setText(autores.getApellidoAutor());
            txt_ApellidoAutor.setEnabled(false);
            
            btn_Accion.setText("Eliminar");
        }
    }

