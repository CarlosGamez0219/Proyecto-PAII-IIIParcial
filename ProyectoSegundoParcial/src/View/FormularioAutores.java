    private void btn_AccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AccionActionPerformed
        // TODO add your handling code here:
        
        if(mode.equals("INS")){
            Autores autores= new Autores ();
            autores.setNombreAutor(txt_NombreAutor.getText());
            autores.setApellidoAutor(txt_ApellidoAutor.getText());
            dao.insert(autores);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
        
        if(mode.equals("UPD")){
            Autores autores= new Autores ();
            autores.setNombreAutor(txt_NombreAutor.getText());
            autores.setAutorID(AutorID);
            autores.setApellidoAutor(txt_ApellidoAutor.getText());
            autores.setAutorID(AutorID);
            
            dao.update(autores);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
        
        if(mode.equals("DLT")){
            dao.delete(AutorID);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
    }//GEN-LAST:event_btn_AccionActionPerformed

