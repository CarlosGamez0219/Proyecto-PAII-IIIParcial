    private void btn_AccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AccionActionPerformed
        // TODO add your handling code here:
        
        if(mode.equals("INS")){
            Rol rol = new Rol();
            rol.setRolDescripcion(txt_RolDescripcion.getText());
            dao.insert(rol);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
        
        if(mode.equals("UPD")){
            Rol rol = new Rol();
            rol.setRolDescripcion(txt_RolDescripcion.getText());
            rol.setRolID(RolID);
            dao.update(rol);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
        
        if(mode.equals("DLT")){
            dao.delete(RolID);
            
            parent.refrescarInfo();
            
            this.dispose();
        }
        
    }//GEN-LAST:event_btn_AccionActionPerformed

