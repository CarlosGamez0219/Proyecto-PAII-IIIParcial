    public FormularioRol(ListaRol pParent, boolean modal, String pMode, int pRolID) {
        super(pParent, modal);
        this.parent = pParent;
        initComponents();
        setLocationRelativeTo(null);
        
         dao = new RolDAO();
        this.mode = pMode;
        this.RolID = pRolID;
        txt_RolID.setText(String.valueOf(pRolID));
        if(mode.equals("INS")){
            btn_Accion.setText("Insertar");
        }else if(mode.equals("UPD")){
            Rol rol = (Rol) dao.getByRolID(RolID);
            txt_RolDescripcion.setText(rol.getRolDescripcion());

            btn_Accion.setText("Actualizar");
            
        }else if(mode.equals("DLT")){
            Rol rol = (Rol) dao.getByRolID(RolID);
            txt_RolDescripcion.setText(rol.getRolDescripcion());
            txt_RolDescripcion.setEnabled(false);
            btn_Accion.setText("Eliminar");
        }
    }

