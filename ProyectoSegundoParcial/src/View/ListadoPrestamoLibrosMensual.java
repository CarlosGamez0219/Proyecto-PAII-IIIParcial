    private void mostrarPrestamoMes() {
        ReportesDAO dao = new ReportesDAO();
        List<PrestamoMes> lista = dao.obtenerPrestamosPorMes();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Mes");
        modelo.addColumn("totalPrestado");

        for (PrestamoMes prestamo : lista) {
            Object[] fila = new Object[2];
            fila[0] = prestamo.getMes();
            fila[1] = prestamo.getTotalPrestado();
            modelo.addRow(fila);
        }

        tbl_PrestamosMensuales.setModel(modelo);
    }

